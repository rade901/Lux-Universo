                                        zastupljen proces u kojem vanjska, završna obloga fasade dijelom reflektira sunčevu toplinu, a dijelom ju emitira u ventilirani prostor, koji pospješuje smanjenje emitirane temperature, a time i bitno smanjuje prijenos
